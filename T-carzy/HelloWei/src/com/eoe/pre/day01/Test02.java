package com.eoe.pre.day01;

public class Test02 {
	public static void main(String[] args) {
		System.out.println("5+3=" + (5 + 3));
		System.out.println("5-3=" + (5 - 3));
		System.out.println("5*3=" + 5 * 3);
		System.out.println("5/3=" + 5 / 3);
		System.out.println("-9的绝对值是："+Math.abs(-9));
		System.out.println("9的平方根是："+Math.sqrt(9));
		System.out.println("(99+99.9)/2的四舍五入是："+Math.round((99+99.9)/2));
		System.out.println("99的9次方是："+Math.pow(99,9));
	}
}
